
public class Calculadora {
    //Va a tener 3 métodos sobrecargados 
    public int sumar (int a, int b){
        return a + b; 
    }
    //No hay poblema en colocr nuevamente las variables en los métodos
    public int sumar(int a, int b, int c){
        return a + b + c; 
    }
    public double sumar(double a, double b){
        return a + b; 
    }
    
}
